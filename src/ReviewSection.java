    private void loadStudentData() {
        try {
            // Fetch list of students with pending enrollments
            List<Object[]> studentList = EnrollmentService.getStudentsWithPendingEnrollments();

            // Update the student table on the left
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear previous rows

            for (Object[] student : studentList) {
                model.addRow(student); // Add each student to the table
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading students: " + e.getMessage());
        }
    }

    private void loadEnrollmentDetails(String studentNim, int semester) {
        try {
            // Fetch enrollment details for the selected student and semester
            List<Object[]> details = EnrollmentService.getEnrollmentDetails(studentNim, semester);

            // Update the enrollment details table (jTable2)
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0); // Clear previous rows

            for (Object[] detail : details) {
                model.addRow(detail); // Add each enrollment detail to the table
            }

            // Update labels with the selected student's name and semester
            nameLabel.setText("Name : " + jTable1.getValueAt(jTable1.getSelectedRow(), 1));
            semesterLabel.setText("Semester : " + semester);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading enrollment details: " + e.getMessage());
        }
    }

