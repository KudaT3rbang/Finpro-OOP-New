    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to accept their enrollment.");
            return;
        }

        String studentNim = (String) jTable1.getValueAt(selectedRow, 0);
        int semester = (int) jTable1.getValueAt(selectedRow, 2);
        String adminUsername = AdminSession.getInstance().getUsername();

        EnrollmentService.updateEnrollmentStatus(studentNim, semester, "Accepted", adminUsername);
        JOptionPane.showMessageDialog(this, "Enrollment accepted successfully!");

        loadStudentData(); // Refresh student list
        ((DefaultTableModel) jTable2.getModel()).setRowCount(0);
    }//GEN-LAST:event_acceptButtonActionPerformed

    private void rejectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to reject their enrollment.");
            return;
        }

        String studentNim = (String) jTable1.getValueAt(selectedRow, 0);
        int semester = (int) jTable1.getValueAt(selectedRow, 2);
        String adminUsername = AdminSession.getInstance().getUsername();

        EnrollmentService.updateEnrollmentStatus(studentNim, semester, "Rejected", adminUsername);
        JOptionPane.showMessageDialog(this, "Enrollment rejected successfully!");

        loadStudentData(); // Refresh student list
        ((DefaultTableModel) jTable2.getModel()).setRowCount(0);
    }//GEN-LAST:event_rejectButtonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) { // Double-click detected
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                String studentNim = (String) jTable1.getValueAt(selectedRow, 0);
                int semester = (int) jTable1.getValueAt(selectedRow, 2);

                // Call method to load enrollment details
                loadEnrollmentDetails(studentNim, semester);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

